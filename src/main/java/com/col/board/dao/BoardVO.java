package com.col.board.dao;

import org.springframework.web.multipart.MultipartFile;

import java.util.List;

public class BoardVO {

    private String boardSeq;                        // 글 번호
    private String boardCnt;                        // 조회수
    private String boardRegDate;                    // 작성일
    private String boardTitle;                      // 글 제목
    private String boardContent;                    // 내용
    private String userID;                          // 작성자
    private String boardDelFlag;                    // 삭제 플래그
    private String boardCate;                       // 게시판 종류
    private List<MultipartFile> boardFile;          // 업로드 파일

    public String getBoardSeq() {
        return boardSeq;
    }

    public void setBoardSeq(String boardSeq) {
        this.boardSeq = boardSeq;
    }

    public String getBoardCnt() {
        return boardCnt;
    }

    public void setBoardCnt(String boardCnt) {
        this.boardCnt = boardCnt;
    }

    public String getBoardRegDate() {
        return boardRegDate;
    }

    public void setBoardRegDate(String boardRegDate) {
        this.boardRegDate = boardRegDate;
    }

    public String getBoardTitle() {
        return boardTitle;
    }

    public void setBoardTitle(String boardTitle) {
        this.boardTitle = boardTitle;
    }

    public String getBoardContent() {
        return boardContent;
    }

    public void setBoardContent(String boardContent) {
        this.boardContent = boardContent;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getBoardDelFlag() {
        return boardDelFlag;
    }

    public void setBoardDelFlag(String boardDelFlag) {
        this.boardDelFlag = boardDelFlag;
    }

    public String getBoardCate() {
        return boardCate;
    }

    public void setBoardCate(String boardCate) {
        this.boardCate = boardCate;
    }

    public List<MultipartFile> getBoardFile() {
        return boardFile;
    }

    public void setBoardFile(List<MultipartFile> boardFile) {
        this.boardFile = boardFile;
    }
}
