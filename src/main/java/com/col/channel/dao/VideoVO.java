package com.col.channel.dao;

public class VideoVO {
	
	private String userID;
	private int videoNum;
	private String videoAddr;
	private String videoTitle;
	private int videoSize;
	private int videoPlayTime;
	private int videoLevel;
	private int videoCate;
	private String videoPack;
	private int videoPrice;
	private int videoView;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getVideoNum() {
		return videoNum;
	}
	public void setVideoNum(int videoNum) {
		this.videoNum = videoNum;
	}
	public String getVideoAddr() {
		return videoAddr;
	}
	public void setVideoAddr(String videoAddr) {
		this.videoAddr = videoAddr;
	}
	public String getVideoTitle() {
		return videoTitle;
	}
	public void setVideoTitle(String videoTitle) {
		this.videoTitle = videoTitle;
	}
	public int getVideoSize() {
		return videoSize;
	}
	public void setVideoSize(int videoSize) {
		this.videoSize = videoSize;
	}
	public int getVideoPlayTime() {
		return videoPlayTime;
	}
	public void setVideoPlayTime(int videoPlayTime) {
		this.videoPlayTime = videoPlayTime;
	}
	public int getVideoLevel() {
		return videoLevel;
	}
	public void setVideoLevel(int videoLevel) {
		this.videoLevel = videoLevel;
	}
	public int getVideoCate() {
		return videoCate;
	}
	public void setVideoCate(int videoCate) {
		this.videoCate = videoCate;
	}
	public String getVideoPack() {
		return videoPack;
	}
	public void setVideoPack(String videoPack) {
		this.videoPack = videoPack;
	}
	public int getVideoPrice() {
		return videoPrice;
	}
	public void setVideoPrice(int videoPrice) {
		this.videoPrice = videoPrice;
	}
	public int getVideoView() {
		return videoView;
	}
	public void setVideoView(int videoView) {
		this.videoView = videoView;
	}
	

}
